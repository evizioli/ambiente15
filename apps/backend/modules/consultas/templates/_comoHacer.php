Para acceder a la información de un punto determinado, debe posicionar el mouse sobre el mapa. Con la rueda del mouse o haciendo clic en el signo + o – del mapa (esquina inferior derecha), puede hacer zoom y para desplazar el mapa y acceder a la vista de otro lugar debe mantener apretado el botón izquierdo del mouse y moverlo.
<br>
<br>
El número en el interior del icono, indica que en esa zona hay esa cantidad de puntos de monitoreos, si aumenta el zoom, el número irá disminuyendo y podrá ver las diferentes ubicaciones individuales. Si cliquea en un icono grande, la información que verá a la izquierda del mapa es la de todos los puntos que están en esa región.
<br>
<br>
La información de cada punto, la verá en una tabla que se despliega en el sector izquierdo de la pantalla que le mostrará la ubicación del punto, y en las columnas los diferentes valores cargados en función de la fecha. 
<br>
<br>
Por debajo de esta información se tendrá una tabla que muestra un resumen de la información con el ultimo dato cargado, el valor mínimo, el máximo y el promedio de los valores hallados para ese punto.

