    public function executeManualDelUsuario(sfWebRequest $request)
    {
        $this->form = new SubirManualForm();
        if ($request->isMethod('post')) {
            $this->form->bind($request->getParameter($this->form->getName()), $request->getFiles($this->form->getName()));
            if ($this->form->isValid()) {
                $v = $this->form->getValues();
                if ($v['consulta']) {
                    rename($v['consulta']->getTempName(), ProjectConfiguration::caminoTutorialConsulta());
                }
                if ($v['acceso']) {
                    rename($v['acceso']->getTempName(), ProjectConfiguration::caminoTutorialAcceso());
                }
                if ($v['acceso']) {
                    rename($v['muni']->getTempName(), ProjectConfiguration::caminoTutorialMuni());
                }
                $this->redirect('mas/manualDelUsuario');
            }
        }
    }

    
